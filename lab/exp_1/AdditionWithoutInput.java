
public class AdditionWithoutInput {
    public static void main(String[] args) {
        
        int num1 = 10;     // first integer
        int num2 = 20;     // second integer
        float num3 = 5.5f; // float value
        
        float sum = num1 + num2 + num3;
        
        System.out.println("First Integer: " + num1);
        System.out.println("Second Integer: " + num2);
        System.out.println("Float Value: " + num3);
        System.out.println("Total Sum: " + sum);
    }
}